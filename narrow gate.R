## Matthew 7:13–14 – Reverse Interlinear

### Verse 13

> **εἰσέλθετε διὰ τῆς στενῆς πύλης·**  
  > *eiselthéte diá tēs stenês pýlēs*  
  > **Enter through the narrow gate;**  
  
  > **ὅτι πλατεῖα ἡ πύλη καὶ εὐρύχωρος ἡ ὁδὸς**  
  > *hóti plateía hē pýlē kaì eurýchōros hē hodós*  
  > **for wide is the gate and broad is the road**  
  
  > **ἡ ἀπάγουσα εἰς τὴν ἀπώλειαν,**  
  > *hē apágousa eis tḕn apṓleian*  
  > **that leads to destruction,**  
  
  > **καὶ πολλοί εἰσιν οἱ εἰσερχόμενοι δι’ αὐτῆς.**  
  > *kaì polloí eisin hoi eiserchómenoi di’ autês*  
  > **and many are those who enter through it.**
  
  ---
  
  ### Verse 14
  
  > **ὅτι στενὴ ἡ πύλη**  
  > *hóti stenḕ hē pýlē*  
  > **For narrow is the gate**  
  
  > **καὶ τεθλιμμένη ἡ ὁδὸς**  
  > *kaì tethlimménē hē hodós*  
  > **and difficult is the road**  
  
  > **ἡ ἀπάγουσα εἰς τὴν ζωήν,**  
  > *hē apágousa eis tḕn zōḗn*  
  > **that leads to life,**  
  
  > **καὶ ὀλίγοι εἰσὶν οἱ εὑρίσκοντες αὐτήν.**  
  > *kaì olígoi eisin hoi heurískontes autḗn*  
  > **and few are those who find it.**
  